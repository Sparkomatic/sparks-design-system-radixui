/**
 * Scans src/tokens/** and writes src/tokens/index.css with @import lines.
 * Runs automatically via predev / prebuild hooks — no manual imports needed.
 */
import { readdirSync, writeFileSync } from "fs";
import { join, relative } from "path";

const TOKENS_DIR = "src/tokens";
const OUT_FILE = join(TOKENS_DIR, "index.css");

function collectCssFiles(dir, base = dir) {
  const files = [];
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    const full = join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...collectCssFiles(full, base));
    } else if (entry.name.endsWith(".css") && entry.name !== "index.css") {
      files.push("./" + relative(base, full));
    }
  }
  // Root-level files before subdirectory files, then alphabetical within each group
  return files.sort((a, b) => {
    const aDepth = a.split("/").length;
    const bDepth = b.split("/").length;
    if (aDepth !== bDepth) return aDepth - bDepth;
    return a.localeCompare(b);
  });
}

const files = collectCssFiles(TOKENS_DIR);
const imports = files.map((f) => `@import "${f}";`).join("\n");
const content = `/* Auto-generated by scripts/sync-token-imports.mjs — do not edit */\n${imports}\n`;

writeFileSync(OUT_FILE, content);
console.log(`[sync-tokens] ${files.length} files → src/tokens/index.css`);
